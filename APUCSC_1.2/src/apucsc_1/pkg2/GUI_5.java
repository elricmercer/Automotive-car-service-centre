    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        //REGISTER MANAGER**************************************************
        boolean flag = false;
        String a,c,d="",e="";
        a = jTextField1.getText().trim();//user id
        char[] b = jPasswordField1.getPassword();
        c = new String(b);
        c = c.trim();//password
        if(a.substring(0,1).equals("M"))
        {
            d = jTextField2.getText().trim();//name
            e = jTextField3.getText().trim();//email
            for(int i=0;i<APUCSC_12.allManager.size();i++)
            {
                if(APUCSC_12.allManager.get(i).getUserId().equals(a)||APUCSC_12.allManager.get(i).getEmail().equals(e))
                {
                    JOptionPane.showMessageDialog(this,"USER ID or Email already in use!");
                    flag = true;
                }
            }
            if(flag==false)
            {
                Manager m = new Manager(a,c,d,e);
                APUCSC_12.allManager.add(m);
                try
                {
                    PrintWriter p = new PrintWriter("manager.txt");
                    for(int i=0;i<APUCSC_12.allManager.size();i++)
                    {
                        p.println(APUCSC_12.allManager.get(i).getUserId());
                        p.println(APUCSC_12.allManager.get(i).getPassword());
                        p.println(APUCSC_12.allManager.get(i).getName());
                        p.println(APUCSC_12.allManager.get(i).getEmail());
                        p.println();
                    }
                    JOptionPane.showMessageDialog(this, "NEW MANAGER REGISTERED");
                    p.close();
                }
                catch(Exception ex)
                {
                    
                }
            }
        }
        else
            JOptionPane.showMessageDialog(this,"Please follow the instructions");
        //REGISTER MANAGER**************************************************
        
    }//GEN-LAST:event_jButton1ActionPerformed

