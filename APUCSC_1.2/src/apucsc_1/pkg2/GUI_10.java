    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        //EDIT TECHS DETAILS***********************************************
        String inUserId = jTextField1.getText().trim();
        String originalId="", originalName="";
        boolean flag=false;
        for(int i=0;i<APUCSC_12.allTechs.size();i++)
        {
            if(APUCSC_12.allTechs.get(i).getUserId().equals(inUserId))
            {
                originalId = APUCSC_12.allTechs.get(i).getUserId();
                originalName = APUCSC_12.allTechs.get(i).getName();
                flag = true;
                break;
            }
        }
        if(flag)
        {
            String newEmail = JOptionPane.showInputDialog("Enter email");
            if(!newEmail.equals(null))
            {
                String newPassword = JOptionPane.showInputDialog("Enter password");
                if(!newPassword.equals(null))
                {
                    String originalFilePath = "techs.txt";
                    String tempFilePath = "temporary.txt";
                    File oldFile = new File(originalFilePath);
                    File newFile = new File(tempFilePath);
                    String user_id="";
                    String password ="";
                    String name ="";
                    String email ="";
                    try
                    {
                        FileWriter fw = new FileWriter(tempFilePath,true);
                        BufferedWriter bw = new BufferedWriter(fw);
                        PrintWriter pw = new PrintWriter(bw);
                        Scanner s = new Scanner(new File(originalFilePath));
                        while(s.hasNext())
                        {
                            user_id = s.nextLine();
                            password = s.nextLine();
                            name = s.nextLine();
                            email = s.nextLine();
                            s.nextLine();
                            if(user_id.equals(inUserId)) 
                            {
                                pw.println(originalId);
                                pw.println(newPassword);
                                pw.println(originalName);
                                pw.println(newEmail);
                                pw.println();
                            }
                            else
                            {
                                pw.println(user_id);
                                pw.println(password);
                                pw.println(name);
                                pw.println(email);
                                pw.println();
                            }
                        }
                        s.close();
                        pw.flush();
                        pw.close();
                        oldFile.delete();
                        File dump = new File(originalFilePath);
                        newFile.renameTo(dump);
                    }
                    catch(Exception e)
                    {

                    }
                    try
                    {
                        APUCSC_12.allTechs = null;
                        Scanner s = new Scanner(new File("techs.txt"));
                        while(s.hasNext())
                        {
                            String a = s.nextLine();//user id
                            String b = s.nextLine();//password
                            String c = s.nextLine();//name
                            String d = s.nextLine();//email
                            s.nextLine();
                            Techs t = new Techs(a,b,c,d);
                            APUCSC_12.allTechs.add(t);
                        }
                        s.close();
                    }
                    catch(Exception e)
                    {

                    }
                }
                else
                    JOptionPane.showMessageDialog(this,"Field cannot be left empty");
            }
            else
                JOptionPane.showMessageDialog(this,"Enter old email or new email! Field cannot be left empty");
        }
        else
            JOptionPane.showMessageDialog(this,"USER NOT FOUND!");

        
        //EDIT TECHS DETAILS***********************************************
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

